function localizeDate(date, lang) {
	if (lang === "en") return date;
	let out = date;
	MONTHS_EN.forEach((m, i) => {
		out = out.replace(new RegExp(`\\b${m}\\b`, "gi"), MONTHS_BN[i]);
	});
	return out.replace(/\d/g, (d) => DIGITS_BN[Number(d)]);
}
